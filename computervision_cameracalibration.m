% Calibrate camera
% Create a set of calibration images.
CalImgFolder = 'Y:\...\ComputerVision\calibration_pics';
addpath(CalImgFolder);
images = imageSet(CalImgFolder);
imageFileNames = images.ImageLocation

% Detect calibration pattern.
[imagePoints, boardSize] = detectCheckerboardPoints(imageFileNames);

% Generate world coordinates of the corners of the squares.
squareSize = 20; % millimeters
worldPoints = generateCheckerboardPoints(boardSize, squareSize);

% Calibrate the camera.
I = readimage(images, 1); 
imageSize = [size(I, 1), size(I, 2)];
[params, ~, estimationErrors] = estimateCameraParameters(imagePoints, worldPoints, ...
                                     'ImageSize', imageSize);

% Visualize camera extrinsics. See below for both locations.
% figure;
% showExtrinsics(params);
% plotting relative locations of the camera and the calibration pattern
figure; 
showExtrinsics(params, 'CameraCentric');

figure; 
showExtrinsics(params, 'PatternCentric');

% Measuring calibration accuracy: Reprojection Errors
figure; 
showReprojectionErrors(params);

% Plot detected and reprojected points.
figure; 
imshow(imageFileNames{1}); 
hold on;
plot(imagePoints(:,1,1), imagePoints(:,2,1),'go');
plot(params.ReprojectedPoints(:,1,1),params.ReprojectedPoints(:,2,1),'r+');
legend('Detected Points','ReprojectedPoints');
hold off;

% Estimation Errors
displayErrors(estimationErrors, params);

